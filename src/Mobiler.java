     public void autoId()
    {
        try {
             Class.forName("com.mysql.jdbc.Driver");
                   con1 = DriverManager.getConnection("jdbc:mysql://localhost/sjshop","root","");
            Statement s = con1.createStatement();
            ResultSet rs = s.executeQuery("SELECT MAX(repairno) FROM repair");
            rs.next();
            rs.getString("MAX(repairno)");
            if (rs.getString("MAX(repairno)") == null) {
                txtrepaino.setText("RE001");     
            } else {
                long id = Long.parseLong(rs.getString("MAX(repairno)").substring(2, rs.getString("MAX(repairno)").length()));
                id++;
                txtrepaino.setText("RE" + String.format("%03d", id));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }      
    }
    
     
       private void table_update() {
        int CC;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/sjshop","root","");
            pst = con1.prepareStatement("SELECT * FROM repair");
             ResultSet Rs = pst.executeQuery();  
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("repairno"));
                    v2.add(Rs.getString("custname"));
                    v2.add(Rs.getString("model"));
                     v2.add(Rs.getString("sn"));
                    v2.add(Rs.getString("price"));
                    v2.add(Rs.getString("pay"));
                    v2.add(Rs.getString("due"));
                    v2.add(Rs.getString("status"));
                }
                DFT.addRow(v2);
                
                    DFT.fireTableDataChanged();

                
                
            }
        } catch (Exception e) {
        }
    }
     
     
     
    
    
