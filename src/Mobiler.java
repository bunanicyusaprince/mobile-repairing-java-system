    public void Save()
    {
          String repno = txtrepaino.getText();        
         String name = txtname.getText();
         String mobile = txtmobile.getText();
         String model = txtmodel.getText();
         String sn = txtsn.getText();
         String problem = txtprb.getText();
         String price = txtrepair.getText();
         String pay = txtpay.getText();
         String Due = txtdue.getText();
         String status = txtstatus.getSelectedItem().toString();         
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/sjshop","root","");
            pst = con1.prepareStatement("insert into repair(repairno,custname,phone,model,sn,problem,price,pay,due,status)values(?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, repno);
            pst.setString(2, name);
            pst.setString(3, mobile);
               pst.setString(4, model);
            pst.setString(5, sn);
            pst.setString(6, problem);
            pst.setString(7, price);
            pst.setString(8, pay);
            pst.setString(9, Due);
            pst.setString(10, status);           
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Employeee Insertedddddd");
           txtrepaino.setText("");
           txtname.setText("");
           txtmobile.setText("");
           txtmodel.setText("");
           txtsn.setText("");
           txtprb.setText("");
           txtrepair.setText("");
           
             txtpay.setText("");
           txtdue.setText("");
           txtstatus.setSelectedIndex(-1);
            txtname.requestFocus();
           
             
          table_update();
          autoId();
          
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Mobiler.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Mobiler.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
